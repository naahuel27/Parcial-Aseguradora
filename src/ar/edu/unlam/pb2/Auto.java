package ar.edu.unlam.pb2;

public class Auto {

	private String marca;
	private String modelo;
	private Integer año;
	private Double sumaAsegurada;
	
	public Auto(String marca, String modelo, Integer año, Double sumaAsegurada) {
		this.sumaAsegurada = sumaAsegurada;
		this.año = año;
		this.marca = marca;
		this.modelo = modelo;
				
	}

}
